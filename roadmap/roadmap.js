// –§—É–Ω–∫—Ü–∏—è –¥–ª—è —Ä–∞–∑–±–ª–æ–∫–∏—Ä–æ–≤–∫–∏ —Å–∫—Ä–æ–ª–ª–∞
function enableScroll() {
    document.body.classList.remove('scroll-locked');
}

// –§—É–Ω–∫—Ü–∏—è –¥–ª—è —Å–æ–∑–¥–∞–Ω–∏—è –º–æ–¥–∞–ª—å–Ω–æ–≥–æ –æ–∫–Ω–∞
function createModal(cardData) {
    console.log('–°–æ–∑–¥–∞–Ω–∏–µ –º–æ–¥–∞–ª—å–Ω–æ–≥–æ –æ–∫–Ω–∞ –¥–ª—è:', cardData.title);
    
    // –ë–ª–æ–∫–∏—Ä—É–µ–º —Å–∫—Ä–æ–ª–ª
    disableScroll();
    
    // –£–¥–∞–ª—è–µ–º —Å—É—â–µ—Å—Ç–≤—É—é—â–µ–µ –º–æ–¥–∞–ª—å–Ω–æ–µ –æ–∫–Ω–æ, –µ—Å–ª–∏ –µ—Å—Ç—å
    const existingModal = document.querySelector('.roadmap-modal');
    if (existingModal) {
        existingModal.remove();
    }

    // –°–æ–∑–¥–∞–µ–º –º–æ–¥–∞–ª—å–Ω–æ–µ –æ–∫–Ω–æ
    const modal = document.createElement('div');
    modal.className = 'roadmap-modal';
    modal.innerHTML = `
        <div class="modal-overlay"></div>
        <div class="modal-content">
            <!-- –ú–æ–±–∏–ª—å–Ω—ã–π –∑–∞–≥–æ–ª–æ–≤–æ–∫ -->
            <div class="mobile-header">
                <h2>${cardData.title}</h2>
                <button class="modal-close" aria-label="–ó–∞–∫—Ä—ã—Ç—å">
                    <img src="../assets/icons/close.png" alt="–ó–∞–∫—Ä—ã—Ç—å">
                </button>
            </div>
            
            <!-- –ö–Ω–æ–ø–∫–∞ –∑–∞–∫—Ä—ã—Ç–∏—è –¥–ª—è –¥–µ—Å–∫—Ç–æ–ø–∞ -->
            <button class="modal-close desktop-close" aria-label="–ó–∞–∫—Ä—ã—Ç—å">
                <img src="../assets/icons/close.png" alt="–ó–∞–∫—Ä—ã—Ç—å">
            </button>
            
            <div class="modal-body">
                <div class="modal-image">
                    <img src="${cardData.image}" alt="${cardData.title}" onerror="this.style.display='none'">
                </div>
                <div class="modal-info">
                    <h2>${cardData.title}</h2>
                    <div class="modal-tags">
                        ${cardData.tags.map(tag => `<span class="modal-tag">${tag}</span>`).join('')}
                    </div>
                    <p class="modal-description">${cardData.fullDescription}</p>
                    <div class="modal-meta">
                        <span class="modal-status ${cardData.status}">${getStatusText(cardData.status)}</span>
                        <span class="modal-date">${cardData.date}</span>
                    </div>
                </div>
            </div>
        </div>
    `;

    // –î–æ–±–∞–≤–ª—è–µ–º –º–æ–¥–∞–ª—å–Ω–æ–µ –æ–∫–Ω–æ –Ω–∞ —Å—Ç—Ä–∞–Ω–∏—Ü—É
    document.body.appendChild(modal);

    // –ü–æ–∫–∞–∑—ã–≤–∞–µ–º –º–æ–¥–∞–ª—å–Ω–æ–µ –æ–∫–Ω–æ
    setTimeout(() => {
        modal.classList.add('show');
    }, 10);

    // –û–±—Ä–∞–±–æ—Ç—á–∏–∫–∏ —Å–æ–±—ã—Ç–∏–π
    const closeBtns = modal.querySelectorAll('.modal-close');
    const overlay = modal.querySelector('.modal-overlay');
    const modalContent = modal.querySelector('.modal-content');

    // –î–æ–±–∞–≤–ª—è–µ–º –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–∏ –¥–ª—è –≤—Å–µ—Ö –∫–Ω–æ–ø–æ–∫ –∑–∞–∫—Ä—ã—Ç–∏—è
    closeBtns.forEach(btn => {
        btn.addEventListener('click', closeModal);
    });
    overlay.addEventListener('click', closeModal);

    // –ó–∞–∫—Ä—ã—Ç–∏–µ –ø–æ Escape
    document.addEventListener('keydown', handleEscape);
    
    // –ü—Ä–µ–¥–æ—Ç–≤—Ä–∞—â–µ–Ω–∏–µ —Å–∫—Ä–æ–ª–ª–∞ –≤–Ω—É—Ç—Ä–∏ –º–æ–¥–∞–ª—å–Ω–æ–≥–æ –æ–∫–Ω–∞ –Ω–∞ –º–æ–±–∏–ª—å–Ω—ã—Ö —É—Å—Ç—Ä–æ–π—Å—Ç–≤–∞—Ö
    modalContent.addEventListener('touchmove', (e) => {
        e.preventDefault();
    }, { passive: false });

    function closeModal() {
        modal.classList.remove('show');
        setTimeout(() => {
            modal.remove();
            document.removeEventListener('keydown', handleEscape);
            // –†–∞–∑–±–ª–æ–∫–∏—Ä—É–µ–º —Å–∫—Ä–æ–ª–ª
            enableScroll();
        }, 300);
    }

    function handleEscape(e) {
        if (e.key === 'Escape') {
            closeModal();
        }
    }
}

// –§—É–Ω–∫—Ü–∏—è –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è —Ç–µ–∫—Å—Ç–∞ —Å—Ç–∞—Ç—É—Å–∞
function getStatusText(status) {
    const savedLang = localStorage.getItem('selectedLanguage') || 'EN';
    const keyMap = {
        'completed': 'status-completed',
        'in-development': 'status-in-development',
        'planned': 'status-planned',
        'discussed': 'status-discussed'
    };
    const key = keyMap[status] || status;
    return getTranslation(key, savedLang);
}

